                        <h4>Details</h4>
                        <p></p>
                        <h5><span>Day 1 - </span>Arrival Manali Full through inspection</h5>
                        <p>
                            Upon your arrival at Manali, go riding on the Himalayan riding premier through the challenging roads along the Kullu Valley. The tricky bends and stretches on the road will help you test the characteristics of the bike
                            and acquaint yourself to handle the motor beast for your Manali Leh Manali biking tour. Enjoy a fun ride to the beautiful Kullu Valley. Overnight at Hotel in Manali.
                        </p>
                        <h5><span>Day 2 - </span>Manali to Jispa 150 km Via Rohtang la pass</h5>
                        <p>
                            Today you will experience the first thrill of high Himalayan biking while passing through the high snow-covered Rohtang Pass (3978 meters). The beautiful mountain pass is the gateway to your bike tour. This first leg of
                            your journey is your first challenge as the roads are really bad and the weather conditions are also uncertain. Overcome your first test, after which you will pass through the Lahaul Valley. Lahaul introduces you to the
                            mountains capped with snow all around. Proceed to Jispa, which is your halt for the day. Enjoy a delicious dinner of the mountains.Overnight at Jispa.
                        </p>
                        <h5><span>Day 3 - </span>Jispa to Serchu 120 km</h5>
                        <p>
                            Today, as you leave Jispa behind, enter the Greater Himalayan landscape. This highway route takes you into the heart of the mountains. Enjoy the splendid view as you pass through the Baralachha Pass (5000 meters above
                            sea level). Keep riding towards Serchu which is the borderline between the states of Jammu &amp; Kashmir and Himachal Pradesh. From there, get to the Serchu camp. Relax for the rest of day and keep a high intake of
                            fluids to keep away AMS (acute mountain sickness). As gradually you will gain altitude you will have to take good care of your health as the high altitude and the low level of Oxygen could be fatal.
                        </p>
                        <h5><span>Day 4 - </span>Serchu to Leh 215 km Via Tanglang la pass</h5>
                        <p>
                            Serchu to Leh via lachung la pass(5,059mts) and Tanglang La pass(5,359mts) which is the second highest pass in the world. Today your riding skills would be tested as you will encounter the roughest road and highest
                            passes along your way. The ride is scenic today with so much of different mountains with different shapes will amaze you along your way. After crossing the Tanglang La pass there are lots of villages which are decorated
                            with lush green fields of barley and willow trees where one really starts getting the feel of Ladakh's diverse culture &amp; landscape. Reach Hotel at Leh and then relax after such tiering ride. Overnight at Hotel in
                            Leh.
                        </p>
                        <h5><span>Day 5 - </span>Khardong la pass 18380 ft World's highest motor able road</h5>
                        <p>
                            Today your day covers a visit to Khardungla Pass at 18380 ft, world's highest motorable pass as the Border road organization's proudly proclaims. In the afternoon, leave for a local city tour of Leh. Explore the
                            attractions of Leh city. The Old Leh palace which overlooks the city is quite a breathtaking beauty. There are many eating joints at the central street which serve a wide variety of cuisines. Spend the evening at any of
                            the garden restaurants which are popular in this place. Return to your hotel for the night.
                        </p>
                        <h5><span>Day 6 - </span>Leh to Pangong Lake 140 km Via Changla pass</h5>
                        <p>
                            Continuing this relaxed yet adventurous Ladakh biking trip, start another day with a delectable breakfast at the hotel. Post breakfast, get ready to take an excursion to Pangong. Ride to the mesmerizing Pangong Lake on
                            the Indo-China Border, which is a sight that no one should miss when traveling to Ladakh. En route, you would be crossing the Changla Pass, the third highest motor-able road in the world. The whole journey is laden with
                            amazing sceneries of never-ending Himalayan mountain ranges. Passing such gorgeous bounties of nature, reach Pangong Lake, one of the largest brackish lakes in Asia, situated at a height of 4,250 meters. Imagine a clear
                            blue lake creating soothing sounds with its waves, a throng of mountains in the background, sand and small pebbles everywhere, and a long road penetrating this gorgeous scenery, leading its way to the ever-expanding
                            enthralling vistas – this is something you would experience on this excursion. After a halt at the lake, proceed back towards Chang La (5500 meters above sea level) and go forward to the army checkpoint at Tangtse.
                            Check-in at the resort and have a comfortable stay for the night.
                        </p>
                        <h5><span>Day 7 - </span>Pangong lake to Leh 140 km Via Chang la pass</h5>
                        <p>
                            Have an early morning breakfast and then head back to Leh Via Changla pass. The ride will take about 4-5hrs. Explore Leh the whole day. Shop at bazaars and visit the Shanti Stupa, the Buddhist white-dome huge structure
                            which overlooks Changspa. Enjoy a great view of the Indus Valley and the Zanskar range. Overnight at Hotel.
                        </p>
                        <h5><span>Day 8 - </span>Head back towards Serchu 215 km</h5>
                        <p>
                            Say goodbye to Leh and head back towards Manali. En-route, you will see Pang, a scenic spot in Ladakh's landscape. Halt at Serchu. Sarchu lies in the middle of the two beautiful destinations, Manali and Leh. Have a
                            comfortable stay in the camp.
                        </p>
                        <h5><span>Day 9 - </span>Serchu to Manali 230 km Via Rohtang la pass</h5>
                        <p>
                            In the morning, begin your ride through the Sarchu plain and reach the Baralacha plain till noon. Enjoy the beauty crossing through Lahaul ranges and reach Rohtang pass. Rohtang pass will challenge you with mini-rivers
                            of mud. Ride through the obstacles to reach Manali. Have a comfortable stay at the hotel in Manali.
                        </p>
                        <h5><span>Day 10 - </span>Departure with sweet memories of Ladakh Biking Adventure and the Himalayas</h5>
                        <p>Departure with sweet memories of Ladakh Biking Adventure and the best riding experience of a lifetime in the Himalayas.</p>
